    public AFDView(AFD automaton) {
        this.automaton = automaton;
        
        DefaultTableModel tModel = new DefaultTableModel(automaton.states.size(), automaton.symbols.length() + 2);
        setTableColumnIdentifiers(tModel, automaton.symbols);
        initComponents();
        tblAutomaton.setModel(tModel);
        setTableRows(automaton.states, tModel);
        this.setVisible(true);
    }
    
    public void setTableColumnIdentifiers(DefaultTableModel tModel, String symbols)
    {
        String[] headers = new String[symbols.length() + 2];
        headers [0] = "";
        headers [headers.length - 1] = "â”¤";
        
        for (int i = 1; i < headers.length - 1; i++)
        {
            headers[i] = symbols.substring(i-1,i);
        }
        tModel.setColumnIdentifiers(headers);
    }
    
    public void setTableRows (List<LamdaState> states, DefaultTableModel tModel)
    {
        for (int i = 0; i < states.size(); i++)
        {
            LamdaState state = states.get(i);
            tModel.setValueAt(i, i, 0);
            for (int j = 1; j < tModel.getColumnCount() - 1; j++)
            {
                tModel.setValueAt(state.transitions.get(tModel.getColumnName(j)), i, j);
            }
            tModel.setValueAt(state.isUptakingState ? "1" : "0", i, tModel.getColumnCount() - 1);
        }
    }

